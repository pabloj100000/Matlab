function run092410()
CreateStimuliLogStart();

global pdStim;

Wait2Start()

% 1600 * 9 = 14400

objContrast = [3 6 12 24]/100;
objSeed = 1;

pdStim = 0;                                 %#ok<NASGU>
RF( ...
    'objSeed', objSeed, ...
    'movieDurationSecs', 1600, ...
    'barsWidth',16, ...
    'objContrast',1, ...
    'waitFrames', 1 ...
    );

for i=1:4

    pdStim=1;                                   %#ok<NASGU>
    UFlickerObj_xSx( ...
        'objContrast', objContrast(i), ...
        'backContrast', 1, ...
        'movieDurationSecs', 1600, ...   
        'objSeed',1 ...
        );

    pdStim=2;
    UFlickerObj_xSx( ...
        'objContrast', objContrast(i), ...
        'backContrast', 1, ...
        'movieDurationSecs', 1600, ... 
        'objSeed',2 ...
        );

end

CreateStimuliLogWrite();
end

