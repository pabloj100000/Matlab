function run041911()
global pdStim screen

% Record for 7840 secs
try
    CreateStimuliLogStart();
    
    
    % Define the rectangles
    [screenX, screenY] = Screen('WindowSize', max(Screen('Screens')));
    center1 =[screenX screenY]/2;    
    objRect1 = GetRects(192, center1);

    Wait2Start('debugging',0)
    
    stimSize=832;
%{
    % 1000s
    pdStim = 0;
    pause(.2)
    RF( ...
        'movieDurationSecs', 1000, ...
        'barsWidth',16, ...
        'objContrast',1, ...
        'waitFrames', 1 ...
        );

    % 340s
    pdStim = pdStim+1;
    pause(.2)
    OMS_identifier_LD('presentationLength',20, 'barsWidth', 16);
    
    % traditional Gatting Experiment switching contrast every 10s
    % BARS in background
    % 1600s
    pdStim = pdStim+1;
    stimSize = 832;
    barsWidth = 16;
    backTex = GetCheckersTex(stimSize, barsWidth, screen, 1);
    pause(.2)
    UflickerObj( ...
        'objContrast', [.03 .06 .12 .24 1], ...
        'rects', objRect1, ...
        'backMode', [0 0 1 0], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'backJitterPeriod', 10, ...
        'objJitterPeriod', 10, ...
        'presentationLength', 10, ...
        'movieDurationSecs', 1600 ...
        );

    % traditional Gatting Experiment switching contrast every 10s
    % and FEM
    % 800s
    pdStim = pdStim+1;
    pause(.2)
    UflickerObj( ...
        'objContrast', [.03 .06 .12 .24 1], ...
        'rects', objRect1, ...
        'backMode', [0 1 0 0], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'backJitterPeriod', 10, ...
        'objJitterPeriod', 10, ...
        'presentationLength', 10, ...
        'movieDurationSecs', 800 ...
        );
%
    % traditional Gatting Experiment switching contrast every 10s
    % and no background
    % 800s
    pdStim = pdStim+1;
    pause(.2)
    UflickerObj( ...
        'objContrast', [.03 .06 .12 .24 1], ...
        'rects', objRect1, ...
        'backMode', [0 0 0 1], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'backJitterPeriod', 10, ...
        'objJitterPeriod', 10, ...
        'presentationLength', 10, ...
        'movieDurationSecs', 800 ...
        );
%}
    
    % Sky doesn't dissapear experiment    
    % 800s
    pause(.2)
    pdStim = pdStim+1;
    StableObject( ...
        'backMode', [0 0 1 1], ...
        'barsWidth', 16, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'presentationLength', 4, ...
        'movieDurationSecs', 32 ...
        );
%}
%
    % short Gating experiment trying BACKGROUND CHECKERS of 3 different
    % sizes
    % 600s
    for i=1:3
        barsWidth = 8*2^i;
        backTex = GetCheckersTex(stimSize, barsWidth, screen, 1);
        pdStim = pdStim+1;
        pause(.2)
        UflickerObj( ...
            'objContrast', .03, ...
            'rects', objRect1, ...
            'backMode', [0 0 1 0], ...
            'backTexture', backTex, ...
            'barsWidth', barsWidth, ...
            'stimSize', stimSize, ...
            'backReverseFreq', 1, ...
            'backJitterPeriod', 10, ...
            'objJitterPeriod', 10, ...
            'presentationLength', 10, ...
            'movieDurationSecs', 200 ...
            );
    end
    
    % Probe gating as a function of object size
    % 1000s
    barsWidth = 16;
    backTex = GetCheckersTex(stimSize, barsWidth, screen, 1);
    for i=1:5
        pdStim = pdStim+1;
        pause(.2)
        objRect1 = GetRects(96+32*i, center1);
        UflickerObj( ...
            'objContrast', .03, ...
            'rects', objRect1, ...
            'backMode', [0 0 1 0], ...
            'backTexture', backTex, ...
            'barsWidth', barsWidth, ...
            'stimSize', stimSize, ...
            'backReverseFreq', 1, ...
            'backJitterPeriod', 10, ...
            'objJitterPeriod', 10, ...
            'presentationLength', 10, ...
            'movieDurationSecs', 200 ...
            );
    end
    
    % How long does it take for gating to go away after eyes are fixed (no
    % eye movements)?
    % 400s
    pdStim = pdStim+1;
    pause(.2)
    UflickerObj( ...
        'objContrast', .03, ...
        'rects', objRect1, ...
        'backMode', [0 0 1 1], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'objJitterPeriod', 10, ...
        'presentationLength', 10, ...
        'movieDurationSecs', 400 ...
        );

    % Probing retina health by testing adaptation without background
    % and steady state
    % 500s
    pdStim = pdStim+1;
    pause(.2)
    objRect1 = GetRects(192, center1);
    UflickerObj( ...
        'objContrast', [.12 1 .24 .06 .03], ...
        'rects', objRect1, ...
        'backMode', [0 0 0 1], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'backJitterPeriod', 100, ...
        'objJitterPeriod', 100, ...
        'presentationLength', 100, ...
        'movieDurationSecs', 500 ...
        );

%}
    CreateStimuliLogWrite();

catch
    
    FinishExperiment();
    psychrethrow(psychlasterror);
end %try..catch..
end
