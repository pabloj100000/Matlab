function run091010()
CreateStimuliLogStart();

global pdStim;

Wait2Start()

% 140+660+300 = 1100 secs
% 1100 * 5 = 5500

objContrast = [3 6 12 24 48]/100;
objSeed = 1;

for i=1:5
    pdStim = 0;                                 %#ok<NASGU>
    RF( ...
        'objSeed', objSeed, ...
        'movieDurationSecs', 140, ...
        'barsWidth',16, ...
        'objContrast',1, ...
        'waitFrames', 1 ...
        );
    objSeed = rand('seed');     % make the RF start were it left of.

    pdStim=1;                                   %#ok<NASGU>
    UFlickerObj_SSF( ...
        'objContrast', objContrast(i), ...
        'backContrast', 1, ...
        'movieDurationSecs', 660, ...   33 secs per presentation, presentationsN = 20 
        'objSeed',1, ...
        'backSeed',2 ...
        );

    pdStim=2;
    GattingTiming(...
        'objContrast', objContrast(i), ...
        'backContrast', 1, ...
        'movieDurationSecs', 300, ...   6 secs per presentation, presentationsN = 50
        'objJitterPeriod',1 ...
        );

end

CreateStimuliLogWrite();
end

