function run050511()
global pdStim screen

% Record for 400 + 1000 + 100 + 500 + 500 * 2 secs = 3000
% Record for 3 hours
try
    CreateStimuliLogStart();    

    Wait2Start('debugging',0)
    
    % Define the rectangles
    objRect = GetRects(192, screen.center);

    
    pdStim = 0;
    StableObject();
    
    pause(.2)
    pdStim = pdStim+1;
    % 1000s
    RF( ...
        'movieDurationSecs', 1000, ...
        'barsWidth',16, ...
        'objContrast',1, ...
        'waitFrames', 1 ...
        );

    % 340s
    pdStim = pdStim+1;
    pause(.2)
    OMS_identifier_LD('presentationLength',20, 'barsWidth', 16);
    
    pdStim = pdStim+1;
    pause(.2)
    Sensitization();
    
    %%%%%%%%%%%%%%%%%%%%%% Changing contrast every 100s %%%%%%%%%%%%%%%%%%%
    % traditional Gatting Experiment switching contrast every 100s
    % reversing 100um Checkers in background
    % 500
    pdStim = pdStim+1;
    stimSize = 832;
    barsWidth = 16;
    backTex = GetCheckersTex(stimSize, barsWidth, screen, 1);
    pause(.2)
    UflickerObj( ...
        'objContrast', [.12 .35 .24 .06 .03], ...
        'rects', objRect, ...
        'backMode', [0 0 1 0], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'backJitterPeriod', 100, ...
        'objJitterPeriod', 100, ...
        'presentationLength', 100, ...
        'movieDurationSecs', 500 ...
        );
    Screen('Close', backTex{1});
    
    %%%%%%%%%%%%%%%%%%%%%% Changing contrast every 1s %%%%%%%%%%%%%%%%%%%
    % traditional Gatting Experiment switching contrast every 1s
    % reversing 100um Checkers in background
    pdStim = pdStim+1;
    stimSize = 832;
    barsWidth = 16;
    backTex = GetCheckersTex(stimSize, barsWidth, screen, 1);
    pause(.2)
    UflickerObj( ...
        'objContrast', [.03 .06 .12 .24 .35], ...
        'rects', objRect, ...
        'backMode', [0 0 1 0], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'backJitterPeriod', 1, ...
        'objJitterPeriod', 1, ...
        'presentationLength', 1, ...
        'movieDurationSecs', 500 ...
        );
    Screen('Close', backTex{1});


    %%%%%%%%%%%%%%%%%%%%%%% information experiment %%%%%%%%%%%%%%%%%%%%%%%%
    pause(.2)
    pdStim = pdStim+1;
    ContrastPhaseInformation(...
        'backContrast', 1, ...
        'objContrasts', [.03 .06 .12 .24 .35], ...
        'objCheckerSize', 192, ...
        'objFreq', 10, ...                 
        'stimSize', 512, ...
        'presentationLength', 16, ...
        'movieDurationSecs', 1100, ...
        'objRect', objRect ...
        );

    CreateStimuliLogWrite();

catch 
    FinishExperiment();
    psychrethrow(psychlasterror);
end %try..catch..
end
