function run090310()
CreateStimuliLogStart();

Wait2Start()

% start with some RF
global pdStim;
pdStim = 0;
RF('movieDurationSecs', 572, ...
   'barsWidth',16, ...
   'objContrast',1, ...
   'waitFrames', 1 ...
   );

pdStim=pdStim+1;
StableObject_xxF( ...
    'backContrast', 1, ...
    'repeatBackSeq', 1, ...
    'movieDurationSecs', 440 ...
);          % lasts 440 secs, 4 intensities * 11 secs *10 repeats.

% 572+440 = 1012 secs
% 660+352 = 1012 secs
% record for 1012*6 = 6072
for i=0:4
    objContrast = 3*2^i/100;
    pdStim=pdStim+1;
    UFlickerObj_SSF( ...
        'objContrast', objContrast, ...
        'backContrast', 1, ...
        'movieDurationSecs', 660, ...
        'objSeed',i, ...
        'backSeed',i+1 ...
    );

    pdStim=pdStim+1;    
    FixedObjPhases_SSx( ...
        'objContrast', objContrast, ...
        'backContrast', 1 ...
);          % lasts 352 secs
end

CreateStimuliLogWrite();
end

