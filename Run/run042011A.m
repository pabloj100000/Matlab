function nextRun()
global pdStim screen

% Record for 7840 secs
try
    CreateStimuliLogStart();
    
    
    % Define the rectangles
    [screenX, screenY] = Screen('WindowSize', max(Screen('Screens')));
    center1 =[screenX screenY]/2;    
    objRect1 = GetRects(192, center1);

    Wait2Start('debugging',0)
    
    % 1000s
    pdStim = 0;
    pause(.2)
    RF( ...
        'movieDurationSecs', 1000, ...
        'barsWidth',16, ...
        'objContrast',1, ...
        'waitFrames', 1 ...
        );

    % 340s
    pdStim = pdStim+1;
    pause(.2)
    OMS_identifier_LD('presentationLength',20, 'barsWidth', 16);
    
    % traditional Gatting Experiment switching contrast every 10s
    % BARS in background
    % 1600s
    pdStim = pdStim+1;
    stimSize = 832;
    barsWidth = 16;
    backTex = GetCheckersTex(stimSize, barsWidth, screen, 1);
    pause(.2)
    UflickerObj( ...
        'objContrast', [.03 .06 .12 .24 1], ...
        'rects', objRect1, ...
        'backMode', [0 0 1 0], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'backJitterPeriod', 10, ...
        'objJitterPeriod', 10, ...
        'presentationLength', 10, ...
        'movieDurationSecs', 1600 ...
        );

    % traditional Gatting Experiment switching contrast every 10s
    % and FEM
    % 800s
    pdStim = pdStim+1;
    pause(.2)
    UflickerObj( ...
        'objContrast', [.03 .06 .12 .24 1], ...
        'rects', objRect1, ...
        'backMode', [0 1 0 0], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'backJitterPeriod', 10, ...
        'objJitterPeriod', 10, ...
        'presentationLength', 10, ...
        'movieDurationSecs', 800 ...
        );
%
    % traditional Gatting Experiment switching contrast every 10s
    % and no background
    % 800s
    pdStim = pdStim+1;
    pause(.2)
    UflickerObj( ...
        'objContrast', [.03 .06 .12 .24 1], ...
        'rects', objRect1, ...
        'backMode', [0 0 0 1], ...
        'backTexture', backTex, ...
        'barsWidth', barsWidth, ...
        'stimSize', stimSize, ...
        'backReverseFreq', 1, ...
        'backJitterPeriod', 10, ...
        'objJitterPeriod', 10, ...
        'presentationLength', 10, ...
        'movieDurationSecs', 800 ...
        );
%}
    CreateStimuliLogWrite();

catch
    
    FinishExperiment();
    psychrethrow(psychlasterror);
end %try..catch..
end
